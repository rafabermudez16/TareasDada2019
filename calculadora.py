def  add ( nume1 , nume2 ):
    return nume1 + nume2

def  substract ( nume1 , nume2 ):
    return nume1 - nume2

def  multiply ( nume1 , nume2 ):
    return nume1 * nume2

def  divide ( nume1 , nume2 ):
    return nume1 / nume2

print (add (  8, 9 ))
print (substract ( 5 , 9 ))
print (multiply ( -3 , 2))
print (divide ( 5 , 1))
